function ExecuteScript() {
	let quyvx = `%Answers%`;
	let list = JSON.parse(quyvx);

    const elements = document.querySelectorAll('.form-control._answer.written_answer');

    elements.forEach(element => {
        element.value = 'Theo bạn các thành viên có nên báo cáo kết quả scan virus cho PM định kỳ hàng tuần hoặc hàng tháng không? Vì sao?';
    });

	const qItems = document.querySelectorAll('.test-content > .q_item');
	
	qItems.forEach(item => {
	    const question_id = item.id;
	    
	    const has = list.find(ele => ele.q_id === question_id);
	
	    if (has) {
	        const aw_true_list = has.aTL || [];
	
	        aw_true_list.forEach(aw_true => {
	            const aw_item = item.querySelector(`#${aw_true}`);
	
	            if (aw_item) {
	                aw_item.click();
	            }
	        });
	    } else {
            const aw_items = item.querySelectorAll('li.aw_item input');

            if (aw_items.length > 0) {
                const randomIndex = Math.floor(Math.random() * aw_items.length);
                const randomItem = aw_items[randomIndex];
                
                // Mô phỏng nhấp chuột vào phần tử ngẫu nhiên
                randomItem.click();
            }
	    }
	});
}